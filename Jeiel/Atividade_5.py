numero = int(input("Digite um numero inteiro:"))
dobro = numero * 2
triplo = numero * 3
quadrado = numero ** 2
raizQuadrada = numero ** (1/2)
raizCubica = numero ** (1/3)

print (f"O dobro do número digitado é: {dobro}")
print (f"O triplo do número digitado é: {triplo}")
print (f"O quadrado do número digitado é: {quadrado}")
print (f"A raiz quadrada número digitado é: {raizQuadrada:.2f}")
print (f"A raiz cúbica do número digitado é: {raizCubica:.2f}")