EntradaDados = int(input('Digite um numero inteiro de 1 a 20: '))
Dobro = EntradaDados *2
Triplo = EntradaDados *3
Quadrado = EntradaDados *4
RaizQuadrado = EntradaDados ** (1/2)
RaizCubica = EntradaDados ** (1/3)

print(f'o número que você digitou: {EntradaDados}')
print(f'o dobro do numero escolhido é: {Dobro}')
print(f'o triplo do numero escolhido é: {Triplo}')
print(f'o número escolhido elevado ao quadrado é: {Quadrado}')
print(f'a raiz quadrada do numero escolhido é: {RaizQuadrado}')
print(f'a raiz cúbica do numero escolhido é: {RaizCubica}')